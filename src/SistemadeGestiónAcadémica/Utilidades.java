
package SistemadeGestiónAcadémica;

/**
 * Clase de utilidades generales del sistema.
 * Contiene métodos estáticos de apoyo que pueden usarse
 * sin necesidad de instanciar la clase.
 */

public class Utilidades {
    
    /**
     * Método estático que muestra un mensaje de bienvenida al sistema.
     * Al ser estático se llama directamente como: Utilidades.mostrarBienvenida()
     */
    
    public static void mostrarBienvenida() {
        System.out.println("============================================");
        System.out.println("  Bienvenido al Sistema de Gestión Académica");
        System.out.println("  Universidad Internacional San Isidro Labrador");
        System.out.println("============================================");
    }
}
 